const isAdult = (users) => {
return(
    users.map( user => {
        // console.log(user)
        let isAdult;

        if(user.age >= 18) {
            isAdult = true
        }else {
            isAdult = false
        }
        
        return {
            ...user,
            isAdult,
        }          
    }))
}

